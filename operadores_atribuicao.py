#atribuição simples#
saldo = 500
print(saldo)

#atribuicao com adicao, subtracao, multiplicacao e divisao#
saldo = 500
saldo += 200
print(saldo)

saldo = 500
saldo -= 200
print(saldo)

saldo = 500
saldo *= 2
print(saldo)

saldo = 500
saldo /= 5
print(saldo)

saldo = 500
saldo //= 5 #divisao inteira#
print(saldo)

#atribuicao com modulo, no modulo o resultado que aparece é o resto da divisao#
saldo = 500
saldo %= 480
print(saldo)

#atribuicao com exponemciacao#
saldo = 80
saldo **= 2
print(saldo)

